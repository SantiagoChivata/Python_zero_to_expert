######### Ciclo for in Python #####################
"""
Un ciclo for en Python  es una estructura de control que permite iterar sobre una secuencia de elementos,
como una lista, una tupla, un diccionario, un conjunto o una cadena de caracteres.
"""

##** vamos a iterar letra por letra en la cadena 'Hola'
cadena = 'Hola'

for letra in cadena:
    print(letra)
else:
    print('\nfin ciclo for')