################ Sintaxis simplificada para if / else ##########################

###*** Sintaxis convencional
condition = True

if condition:
    print('True condition')
else:
    print('False condition')
print('\n')


###*** Sintaxis Sinplificada
condition = False
print('True condition') if condition else print('False condition')

###NOTA: la sintaxis simplificada solo se usa en codigo muy simple, no es recomendada en codigo mas complejo 