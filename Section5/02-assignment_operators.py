## Operador de Asignacion '='
miVariable = 10
print(miVariable)
print('\n')

## Operador de Reasignacion '+=' รณ '-=' รณ '*=' รณ '/='
miVariable = miVariable + 1
print("Reasignacion con la forma tradicional:",miVariable)
miVariable += 1 #forma reducida y mas usada por ser optima al tener menos codigo
print("Reasignacion con la forma reducida '+=' :",miVariable)
miVariable -= 2
print("Reasignacion con la forma reducida '-=' :",miVariable)
miVariable *= 5
print("Reasignacion con la forma reducida '*=' :",miVariable)
miVariable /= 2
print("Reasignacion con la forma reducida '/=' :",miVariable)
miVariable //= 2
print("Reasignacion con la forma reducida '//=' :",miVariable)
miVariable %= 5
print("Reasignacion con la forma reducida '%=' :",miVariable)
miVariable **= 5
print("Reasignacion con la forma reducida '**=' :",miVariable)
print('\n')



